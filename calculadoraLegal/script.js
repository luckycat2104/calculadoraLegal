function Soma(){
    var somado = parseInt(prompt("Qual numero o senhorio quer somar?"));
    var somadinho = parseInt(prompt("Qual o outro numero pra somar?"));
    var resultadoSoma = somado + somadinho;
    alert(resultadoSoma);
    }

    function Subtracao(){
        var sub = parseInt(prompt("Qual numero o senhorio quer subtrair?"));
        var subzinho = parseInt(prompt("Qual o outro numero pra subtrair?"));
        var resultadoSub = sub - subzinho;
        alert(resultadoSub);
        }